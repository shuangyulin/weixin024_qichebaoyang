<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												baoyangbianhao: this.getUUID(),
																baoyangmingcheng: '',
																baoyangxinxi: '',
																xiangguantupian: '',
																youxiaoqi: '',
																mendianmingcheng: '',
																thumbsupnum: '',
																crazilynum: '',
																price: '',
												},
																																																				mendianmingchengOptions: [],
				mendianmingchengIndex: 0,
																																				// 登陆用户信息
				user: {},
                                ro:{
                                   baoyangbianhao : false,
                                   baoyangmingcheng : false,
                                   baoyangxinxi : false,
                                   xiangguantupian : false,
                                   youxiaoqi : false,
                                   mendianmingcheng : false,
                                   thumbsupnum : false,
                                   crazilynum : false,
                                   price : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																																										// 下拉框
			res = await this.$api.option(`mendianxinxi`,`mendianmingcheng`,{});
			this.mendianmingchengOptions = res.data;
																											
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`baoyangxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='baoyangbianhao'){
					this.ruleForm.baoyangbianhao = obj[o];
					this.ro.baoyangbianhao = true;
					continue;
					}
					if(o=='baoyangmingcheng'){
					this.ruleForm.baoyangmingcheng = obj[o];
					this.ro.baoyangmingcheng = true;
					continue;
					}
					if(o=='baoyangxinxi'){
					this.ruleForm.baoyangxinxi = obj[o];
					this.ro.baoyangxinxi = true;
					continue;
					}
					if(o=='xiangguantupian'){
					this.ruleForm.xiangguantupian = obj[o];
					this.ro.xiangguantupian = true;
					continue;
					}
					if(o=='youxiaoqi'){
					this.ruleForm.youxiaoqi = obj[o];
					this.ro.youxiaoqi = true;
					continue;
					}
					if(o=='mendianmingcheng'){
					this.ruleForm.mendianmingcheng = obj[o];
					this.ro.mendianmingcheng = true;
					continue;
					}
					if(o=='thumbsupnum'){
					this.ruleForm.thumbsupnum = obj[o];
					this.ro.thumbsupnum = true;
					continue;
					}
					if(o=='crazilynum'){
					this.ruleForm.crazilynum = obj[o];
					this.ro.crazilynum = true;
					continue;
					}
					if(o=='price'){
					this.ruleForm.price = obj[o];
					this.ro.price = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																												
			// 多级联动参数
																																																												
																																	youxiaoqiChange(e) {
				this.ruleForm.youxiaoqi = e.target.value;
				this.$forceUpdate();
			},
																														
																																																									
																																							// 下拉变化
			mendianmingchengChange(e) {
				this.mendianmingchengIndex = e.target.value
				this.ruleForm.mendianmingcheng = this.mendianmingchengOptions[this.mendianmingchengIndex]
			},
																								
																																				xiangguantupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.xiangguantupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																																						
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.baoyangbianhao)){
					this.$utils.msg(`保养编号不能为空`);
					return
				}
																																								if((!this.ruleForm.baoyangmingcheng)){
					this.$utils.msg(`保养名称不能为空`);
					return
				}
																																																																																																																																																																												if(this.ruleForm.thumbsupnum&&(!this.$validate.isIntNumer(this.ruleForm.thumbsupnum))){
					this.$utils.msg(`赞应输入整数`);
					return
				}
																																								if(this.ruleForm.crazilynum&&(!this.$validate.isIntNumer(this.ruleForm.crazilynum))){
					this.$utils.msg(`踩应输入整数`);
					return
				}
																																				if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
																if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
																												if(this.ruleForm.id){
					await this.$api.update(`baoyangxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`baoyangxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

