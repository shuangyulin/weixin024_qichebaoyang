<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												mendianmingcheng: '',
																yuangonggonghao: '',
																yuangongxingming: '',
																yuangongxingbie: '',
																lianxidianhua: '',
																yuangongtupian: '',
												},
												mendianmingchengOptions: [],
				mendianmingchengIndex: 0,
																																yuangongxingbieOptions: [],
				yuangongxingbieIndex: 0,
																												// 登陆用户信息
				user: {},
                                ro:{
                                   mendianmingcheng : false,
                                   yuangonggonghao : false,
                                   yuangongxingming : false,
                                   yuangongxingbie : false,
                                   lianxidianhua : false,
                                   yuangongtupian : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																										
												// 下拉框
			res = await this.$api.option(`mendianxinxi`,`mendianmingcheng`,{});
			this.mendianmingchengOptions = res.data;
																								// 自定义下拉框值
			this.yuangongxingbieOptions = "男,女".split(',')
																					
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yuangongxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='mendianmingcheng'){
					this.ruleForm.mendianmingcheng = obj[o];
					this.ro.mendianmingcheng = true;
					continue;
					}
					if(o=='yuangonggonghao'){
					this.ruleForm.yuangonggonghao = obj[o];
					this.ro.yuangonggonghao = true;
					continue;
					}
					if(o=='yuangongxingming'){
					this.ruleForm.yuangongxingming = obj[o];
					this.ro.yuangongxingming = true;
					continue;
					}
					if(o=='yuangongxingbie'){
					this.ruleForm.yuangongxingbie = obj[o];
					this.ro.yuangongxingbie = true;
					continue;
					}
					if(o=='lianxidianhua'){
					this.ruleForm.lianxidianhua = obj[o];
					this.ro.lianxidianhua = true;
					continue;
					}
					if(o=='yuangongtupian'){
					this.ruleForm.yuangongtupian = obj[o];
					this.ro.yuangongtupian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																										
			// 多级联动参数
																																										
																																							
																																							
									// 下拉变化
			mendianmingchengChange(e) {
				this.mendianmingchengIndex = e.target.value
				this.ruleForm.mendianmingcheng = this.mendianmingchengOptions[this.mendianmingchengIndex]
			},
																								// 下拉变化
			yuangongxingbieChange(e) {
				this.yuangongxingbieIndex = e.target.value
				this.ruleForm.yuangongxingbie = this.yuangongxingbieOptions[this.yuangongxingbieIndex]
			},
																		
																																																						yuangongtupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.yuangongtupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
									
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																				if((!this.ruleForm.yuangonggonghao)){
					this.$utils.msg(`员工工号不能为空`);
					return
				}
																																								if((!this.ruleForm.yuangongxingming)){
					this.$utils.msg(`员工姓名不能为空`);
					return
				}
																																																																																																																																				if(this.ruleForm.id){
					await this.$api.update(`yuangongxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`yuangongxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

